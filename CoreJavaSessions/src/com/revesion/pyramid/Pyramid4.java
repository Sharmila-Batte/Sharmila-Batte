package com.revesion.pyramid;

public class Pyramid4 {

	public static void main(String[] args) {
		int k=3;
		
		
	for(int i=0;i<=3;i++)
	{
		for(int j=1;j<=i;j++)
		{
			System.out.print(k);
			System.out.print(" ");
			k=k+3;
		}
		System.out.println("");
	}
	

	}

}
