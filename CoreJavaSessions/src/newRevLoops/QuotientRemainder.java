package newRevLoops;

public class QuotientRemainder {

	public static void main(String[] args) {
		int no1=10;
		int no2=5;
		int quotient;
		int rem;
		System.out.println("no1: "+no1);
		System.out.println("no2: "+no2);
		quotient=no1/no2;
		System.out.println("quotient: "+quotient);
		rem=no1%no2;
		System.out.println("rem: "+rem);
	}

}
